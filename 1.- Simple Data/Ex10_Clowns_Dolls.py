# Calculate the weight of clowns (112g) and dolls (75g) in a box
clowns_quantity = int(input("Enter the number of clowns: "))
dolls_quantity = int(input("Enter the number of clowns: "))

total_weight = (clowns_quantity * 112) + (dolls_quantity * 75)

print(f"The total weight of the box is {total_weight} grams.")